public class Expressions {
    public void ehh (String[] args) {
        // simple operator basiscs
        int friends = 10;
        friends = friends + 1;
        System.out.println(friends); //this will display 11
        friends = friends % 3; // this will give the remainder of how many times 10 is split 3 ways which would be 1
        friends ++; //this is a shorter way of saying friends = friends + 1; where it increments by 1  

    }
}
